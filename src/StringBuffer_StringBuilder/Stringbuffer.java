package StringBuffer_StringBuilder;

public class Stringbuffer {
public static void main(String[] args) {
	
	StringBuffer s=new StringBuffer("Welcome");
    System.out.println("Actual String : "+s);
    System.out.println("Reverse of an String :  "+s.reverse());
    
    
    StringBuilder s1=new StringBuilder("Anil Gowda N");
    System.out.println("Actual String : "+s1);
    System.out.println("Reverse of an String :  "+s1.reverse());
    
    
    
	}

}
